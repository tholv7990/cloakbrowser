export function maskEndpoint(
  scheme: Proxy['scheme'],
  host: string,
  port: number | null,
  hasCreds: boolean,
): string {
  if (scheme === 'direct') return 'direct://';
  const auth = hasCreds ? '***:***@' : '';
  return `${scheme}://${auth}${host}:${port ?? ''}`;
}

/** Cheap deterministic 64-hex config hash from a seed (mock only). */
export function fakeConfigHash(seed: string): string {
  let h = 2166136261;
  for (let i = 0; i < seed.length; i += 1) {
    h ^= seed.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  const base = (h >>> 0).toString(16).padStart(8, '0');
  return base.repeat(8).slice(0, 64);
}

